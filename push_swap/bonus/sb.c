#include "push_swap_bonus.h"

void	sb(t_list **b)
{
	t_list	*temp;

	if (!(*b) || !(*b)->next)
		return ;
	temp = (*b)->next;
	(*b)->next = temp->next;
	temp->next = (*b);
	(*b) = temp;
}
