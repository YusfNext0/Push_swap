#include "push_swap_bonus.h"

void	ra(t_list **a)
{
	t_list	*temp;

	if (!(*a) || !(*a)->next)
		return ;
	temp = (*a);
	(*a) = (*a)->next;
	temp->next = NULL;
	lstadd_back(a, temp);
}
