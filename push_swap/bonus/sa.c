#include "push_swap_bonus.h"

void	sa(t_list **a)
{
	t_list	*temp;

	if (!(*a) || !(*a)->next)
		return ;
	temp = (*a)->next;
	(*a)->next = temp->next;
	temp->next = (*a);
	(*a) = temp;
}
