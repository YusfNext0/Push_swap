#include "push_swap.h"

void	pa(t_list **a, t_list **b)
{
	t_list	*temp;

	if (!(*b))
		return ;
	temp = (*b);
	(*b) = (*b)->next;
	temp->next = (*a);
	(*a) = temp;
	write (1, "pa\n", 3);
}
